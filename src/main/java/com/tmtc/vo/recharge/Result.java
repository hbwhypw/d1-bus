package com.tmtc.vo.recharge;

public class Result {

	private String key;
	
	private String sign;

	public Result() {
		super();
	}

	public Result(String key, String sign) {
		super();
		this.key = key;
		this.sign = sign;
	}

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getSign() {
		return sign;
	}

	public void setSign(String sign) {
		this.sign = sign;
	}

	@Override
	public String toString() {
		return "Result [key=" + key + ", sign=" + sign + "]";
	}
}
