package com.tmtc.po;

import java.util.Date;

public class LineOrder {
    private String id;

    private String order_no;

    private String user_id;

    private Integer city_id;

    private String line_id;

    private String time_line_id;

    private String station_id;

    private Date order_time;

    private Integer order_status;

    private Integer order_exp;

    private Integer order_type;

    private Date scan_time;

    private Integer is_check;

    private Line line;
    
    private LineTime lineTime;
    
    public LineOrder() {	}
    
    public LineOrder(String id, String order_no, String user_id, Integer city_id, String line_id, String time_line_id,
			String station_id, Date order_time, Integer order_status, Integer order_exp, Integer order_type,
			Date scan_time, Integer is_check) {
		super();
		this.id = id;
		this.order_no = order_no;
		this.user_id = user_id;
		this.city_id = city_id;
		this.line_id = line_id;
		this.time_line_id = time_line_id;
		this.station_id = station_id;
		this.order_time = order_time;
		this.order_status = order_status;
		this.order_exp = order_exp;
		this.order_type = order_type;
		this.scan_time = scan_time;
		this.is_check = is_check;
	}


	public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getOrder_no() {
        return order_no;
    }

    public void setOrder_no(String order_no) {
        this.order_no = order_no;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public Integer getCity_id() {
        return city_id;
    }

    public void setCity_id(Integer city_id) {
        this.city_id = city_id;
    }

    public String getLine_id() {
        return line_id;
    }

    public void setLine_id(String line_id) {
        this.line_id = line_id;
    }

    public String getTime_line_id() {
        return time_line_id;
    }

    public void setTime_line_id(String time_line_id) {
        this.time_line_id = time_line_id;
    }

    public String getStation_id() {
        return station_id;
    }

    public void setStation_id(String station_id) {
        this.station_id = station_id;
    }

    public Date getOrder_time() {
        return order_time;
    }

    public void setOrder_time(Date order_time) {
        this.order_time = order_time;
    }

    public Integer getOrder_status() {
        return order_status;
    }

    public void setOrder_status(Integer order_status) {
        this.order_status = order_status;
    }

    public Integer getOrder_exp() {
        return order_exp;
    }

    public void setOrder_exp(Integer order_exp) {
        this.order_exp = order_exp;
    }

    public Integer getOrder_type() {
        return order_type;
    }

    public void setOrder_type(Integer order_type) {
        this.order_type = order_type;
    }

    public Date getScan_time() {
        return scan_time;
    }

    public void setScan_time(Date scan_time) {
        this.scan_time = scan_time;
    }

    public Integer getIs_check() {
        return is_check;
    }

    public void setIs_check(Integer is_check) {
        this.is_check = is_check;
    }

	public Line getLine() {
		return line;
	}

	public void setLine(Line line) {
		this.line = line;
	}

	public LineTime getLineTime() {
		return lineTime;
	}

	public void setLineTime(LineTime lineTime) {
		this.lineTime = lineTime;
	}

	@Override
	public String toString() {
		return "LineOrder [id=" + id + ", order_no=" + order_no + ", user_id=" + user_id + ", city_id=" + city_id
				+ ", line_id=" + line_id + ", time_line_id=" + time_line_id + ", station_id=" + station_id
				+ ", order_time=" + order_time + ", order_status=" + order_status + ", order_exp=" + order_exp
				+ ", order_type=" + order_type + ", scan_time=" + scan_time + ", is_check=" + is_check + "]";
	}
}