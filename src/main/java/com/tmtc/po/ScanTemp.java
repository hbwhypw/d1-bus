package com.tmtc.po;

import java.util.Date;

public class ScanTemp {
    private String id;

    private String user_id;

    private String linetime_id;

    private String station_id;

    private Date create_time;

    private Integer status;

    private Integer is_check;

    public ScanTemp() {	}
    
    public ScanTemp(String id, String user_id, String linetime_id, String station_id, Date create_time, Integer status,
			Integer is_check) {
		super();
		this.id = id;
		this.user_id = user_id;
		this.linetime_id = linetime_id;
		this.station_id = station_id;
		this.create_time = create_time;
		this.status = status;
		this.is_check = is_check;
	}

	public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUser_id() {
        return user_id;
    }

    public void setUser_id(String user_id) {
        this.user_id = user_id;
    }

    public String getLinetime_id() {
        return linetime_id;
    }

    public void setLinetime_id(String linetime_id) {
        this.linetime_id = linetime_id;
    }

    public String getStation_id() {
        return station_id;
    }

    public void setStation_id(String station_id) {
        this.station_id = station_id;
    }

    public Date getCreate_time() {
        return create_time;
    }

    public void setCreate_time(Date create_time) {
        this.create_time = create_time;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Integer getIs_check() {
        return is_check;
    }

    public void setIs_check(Integer is_check) {
        this.is_check = is_check;
    }

	@Override
	public String toString() {
		return "ScanTemp [id=" + id + ", user_id=" + user_id + ", linetime_id=" + linetime_id + ", station_id="
				+ station_id + ", create_time=" + create_time + ", status=" + status + ", is_check=" + is_check + "]";
	}
    
}