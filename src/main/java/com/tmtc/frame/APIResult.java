package com.tmtc.frame;

/**
 * 返回appAPI的类型
 * @author Administrator
 *
 */
public class APIResult {

	private String session;
	private String msg;
	private int status;
	private Object data;

	public APIResult() {
		super();
	}

	public APIResult(String session, String msg, int status, Object data) {
		super();
		this.session = session;
		this.msg = msg;
		this.status = status;
		this.data = data;
	}

	public String getSession() {
		return session;
	}

	public void setSession(String session) {
		this.session = session;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public Object getData() {
		return data;
	}

	public void setData(Object data) {
		this.data = data;
	}

	@Override
	public String toString() {
		return "APIResult [session=" + session + ", msg=" + msg + ", status=" + status + ", data=" + data + "]";
	}
}
